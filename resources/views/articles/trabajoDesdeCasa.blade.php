                            <p>Nota: Para las pequeñas empresas, ser un administrador de redes sociales generalmente requiere la creación de estrategias (ideas) para diferentes redes sociales, que pueden jugar otros roles en la dirección del marketing en las grandes empresas. Luego, es posible que se le solicite <strong>realizar funciones correspondientes a community managers</strong>.</p>
